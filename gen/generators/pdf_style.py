from reportlab.lib.styles import ParagraphStyle, ListStyle, StyleSheet1
from reportlab.lib.colors import black, Color
from reportlab.lib.enums import TA_CENTER

def getSampleStyleSheet():
    """Returns a stylesheet using Plus Jakarta Sans fonts"""
    stylesheet = StyleSheet1()

    stylesheet.add(ParagraphStyle(name='Normal',
                                  fontName="PlusJakartaSans-Regular",
                                  fontSize=10,
                                  leading=12)
                   )
    stylesheet.add(ParagraphStyle(name='Small',
                                  fontName="PlusJakartaSans-SemiBoldItalic",
                                  parent=stylesheet['Normal'],
                                  fontSize=7)
                   )
    stylesheet.add(ParagraphStyle(name='BodyText',
                                  parent=stylesheet['Normal'],
                                  spaceBefore=6)
                   )
    stylesheet.add(ParagraphStyle(name='Italic',
                                  parent=stylesheet['BodyText'],
                                  fontName="PlusJakartaSans-Italic")
                   )
    stylesheet.add(ParagraphStyle(name='Heading1',
                                  parent=stylesheet['Normal'],
                                  fontName="PlusJakartaSans-SemiBold",
                                  fontSize=18,
                                  leading=22,
                                  spaceAfter=6),
                   alias='h1')
    stylesheet.add(ParagraphStyle(name='Title',
                                  parent=stylesheet['Normal'],
                                  fontName="PlusJakartaSans-SemiBold",
                                  fontSize=18,
                                  leading=22,
                                  alignment=TA_CENTER,
                                  spaceAfter=6),
                   alias='title')
    stylesheet.add(ParagraphStyle(name='Heading2',
                                  parent=stylesheet['Normal'],
                                  fontName="PlusJakartaSans-SemiBold",
                                  fontSize=14,
                                  leading=18,
                                  spaceBefore=12,
                                  spaceAfter=6),
                   alias='h2')
    stylesheet.add(ParagraphStyle(name='Heading3',
                                  parent=stylesheet['Normal'],
                                  fontName="PlusJakartaSans-SemiBoldItalic",
                                  fontSize=12,
                                  leading=14,
                                  spaceBefore=12,
                                  spaceAfter=6),
                   alias='h3')
    stylesheet.add(ParagraphStyle(name='Heading4',
                                  parent=stylesheet['Normal'],
                                  fontName="PlusJakartaSans-SemiBoldItalic",
                                  fontSize=10,
                                  leading=12,
                                  spaceBefore=10,
                                  spaceAfter=4),
                   alias='h4')
    stylesheet.add(ParagraphStyle(name='Heading5',
                                  parent=stylesheet['Normal'],
                                  fontName="PlusJakartaSans-SemiBold",
                                  fontSize=9,
                                  leading=10.8,
                                  spaceBefore=8,
                                  spaceAfter=4),
                   alias='h5')
    stylesheet.add(ParagraphStyle(name='Heading6',
                                  parent=stylesheet['Normal'],
                                  fontName="PlusJakartaSans-SemiBold",
                                  fontSize=7,
                                  leading=8.4,
                                  spaceBefore=6,
                                  spaceAfter=2),
                   alias='h6')
    stylesheet.add(ParagraphStyle(name='Bullet',
                                  parent=stylesheet['Normal'],
                                  firstLineIndent=0,
                                  spaceBefore=3),
                   alias='bu')
    stylesheet.add(ParagraphStyle(name='Tech',
                                  parent=stylesheet['Normal'],
                                  fontName="PlusJakartaSans-Italic",
                                  fontSize=8,
                                  leading=10,
                                  textColor=Color(0.4, 0.4, 0.4),
                                  spaceBefore=1),
                   alias='tech')
    stylesheet.add(ParagraphStyle(name='Definition',
                                  parent=stylesheet['Normal'],
                                  firstLineIndent=0,
                                  leftIndent=36,
                                  bulletIndent=0,
                                  spaceBefore=6,
                                  bulletFontName="PlusJakartaSans-SemiBoldItalic"),
                   alias='df')
    stylesheet.add(ParagraphStyle(name='Code',
                                  parent=stylesheet['Normal'],
                                  fontName='Courier',
                                  fontSize=8,
                                  leading=8.8,
                                  firstLineIndent=0,
                                  leftIndent=36,
                                  hyphenationLang=''))
    stylesheet.add(ListStyle(name='UnorderedList',
                                parent=None,
                            ),
                   alias='ul')
    stylesheet.add(ListStyle(name='OrderedList',
                                parent=None,
                            ),
                   alias='ol')
    return stylesheet
